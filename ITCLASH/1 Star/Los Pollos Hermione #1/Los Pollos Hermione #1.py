output = "LPH: "
i = 0

class bill:
    def __init__(self, item):
        self.item = item

    def curitem(self):
        allak = "Item " + str(i+1) + ": "
        allak += self.item
        return allak
    
loopamount = int(input())
for i in range(loopamount):
    myitem = input()
    totaled = bill(myitem)
    output += totaled.curitem()
    if i == loopamount-1:
        break
    else:
        output += " -> "
print(output)



